const listeners = [];
let current = localStorage.getItem('rs-lang') || 'fr';

export function getLang() { return current; }
export function isFr() { return current === 'fr'; }
export function t(key) { return (T[current] && T[current][key]) || key; }

export function toggleLang() {
  current = current === 'fr' ? 'en' : 'fr';
  localStorage.setItem('rs-lang', current);
  location.reload();
}

export function onLangChange(cb) {
  listeners.push(cb);
}

